
//Ссылочный тип данных String
public class Strings {
    public static void main(String[] args){
        String myString = "Hello"; // String - ссылочный тип данных, является класом, поэтому с большой буквы
        // Hello  - объект, myString - переменная, которая ссылается на этот объект
        String name = "Bob";
        System.out.println(myString + " " + name);
        System.out.println(name + 25 + 25); //Bob2525

    }
}
